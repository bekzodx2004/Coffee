yopiq = true;
body = document.querySelectorAll(".dropdown__body");
aylan = document.querySelectorAll("#aylan");

function ochil1() {
  if (yopiq) {
    body[0].classList.add("ochiq");
    aylan[0].classList.add("aylan");
    yopiq = false;
  } else {
    body[0].classList.remove("ochiq");
    aylan[0].classList.remove("aylan");
    yopiq = true;
  }
}

